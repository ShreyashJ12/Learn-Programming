    public boolean storeData(String fn, String ln, String un, String pass1, String email, String f_path,String class1) 
    {
        String epass=encryptPass.encrypt(pass1);
        try(Connection con=DBUtil.getConnection())
        {
            PreparedStatement pr=con.prepareStatement("INSERT into user values (null,?,?,?,?,?,?,?)");
            
            pr.setString(1,fn);
            pr.setString(2,ln);
            pr.setString(3,un);
            pr.setString(4,email);
            pr.setString(5,epass);
            pr.setString(6,f_path);
            
            pr.setString(7,class1);
            pr.execute();
            
            return true;
        }
        catch(SQLException e)
        {
            e.printStackTrace();
            System.out.println(e);
            return false;
        }

    }
