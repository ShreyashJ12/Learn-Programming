    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        // TODO add your handling code here:
        
          fn=fnt.getText().trim().toLowerCase();
        ln=lnt.getText().trim().toLowerCase();
        un=unt.getText().trim().toLowerCase();
        pass1=new String(pass1t.getPassword()).trim();
        pass2=new String(pass2t.getPassword()).trim();
        email=emailt.getText().trim().toLowerCase();
        f_path=f_patht.getText().trim();
        class1=class2.getText().trim();
        
        
        
        String regex1 = "^[a-zA-Z]+$";
        String regex2 = "^[a-zA-Z0-9]+$";
        
        try
        {
                //null check
            if(fn.equals("") || ln.equals("") || un.equals("") || pass1.equals("") || pass2.equals("") || email.equals("") || f_path.equals("") || class1.equals("") )
            {
                
                jTextArea1.append("\nAny field cannot be null");
            }
            else        //fields validations
            {
                if (fn.matches(regex1) && ln.matches(regex1)) {  //fn ln condtion check
                    if(un.matches(regex2))      //un condtion check
                    {
                        //boolean c=true;
                        String c=this.checkUN(un);
                        
                        if("false".equals(c))
                        {
                            jTextArea1.append("\nUserName is already Taken");
                        }
                        else if("error".equals(c))
                            jTextArea1.append("\nError occurred");
                        else if("true".equals(c))
                        {
                            if(pass1.equals(pass2))                //Final password & Email Check
                            {
                               
                                try{
                                    InternetAddress in=new InternetAddress(email);
                                    in.validate();
                                   
                                    String e=this.checkEmail(email);
                        
                                    if("false".equals(e))
                                    {
                                        jTextArea1.append("\nEmail is already Taken");
                                    }
                                    else if("error".equals(e))
                                        jTextArea1.append( "\nError occurred");
                                    
                                    else if("true".equals(e))
                                    {
                                        boolean s=this.storeData(fn, ln, un, pass1, email, f_path,class1);
                                        if(s==true)                                                        //Final Check
                                        {
                                            Thread t1=new Thread(()->{new SendEmail().registerUserConfirmation(email, un, fn, ln, pass1);});
                                            Thread t2=new Thread(()->{jTextArea1.setText("User "+fn+" "+ln+" added successfully"); });
                                            
                                            t1.start();
                                            t2.start();
                                            
                                        }
                                        else
                                           jTextArea1.append("\nData did not get registered");
                                        }
                                }
                                catch(AddressException ae)
                                {
                                    jTextArea1.append("\nEmail not valid");
                                }
                            }
                            else
                            {
                                jTextArea1.append( "\nPassword doesn't match");
                                pass1t.setText("");
                                pass2t.setText("");
                            }
                        }
                    }
                    else
                    {
                        jTextArea1.append("\nUser name can only take alphabets and numbers");
                        lnt.setText("");
                        pass1t.setText("");
                        pass2t.setText("");
                    }
                }
                else                                    //fn ln condtion check
                {
                    jTextArea1.append("\nFirst name and Last name should only consist alphabets");
                    fnt.setText("");
                    lnt.setText("");
                    pass1t.setText("");
                    pass2t.setText("");
                }
            }
             }
            catch(Exception e)
            {
                    jTextArea1.append( "\nSomething went wrong");
            }
    }//GEN-LAST:event_jButton4ActionPerformed

