    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().trim().equals("") && !jEditorPane1.getText().trim().equals(""))
        {
            title=jTextField1.getText().trim();
            post=jEditorPane1.getText().trim();
            date=new Date();
            SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            try
            {
                Connection con=DBUtil.getConnection();
                PreparedStatement pr=con.prepareStatement("insert into feed values(null,"+uid+",'"+title+"','"+post+"',default,'"+sdf.format(date)+"')");
                
                pr.execute();
                JOptionPane.showMessageDialog(this, "Posted Successfully");
                jTextField1.setText("");
                jEditorPane1.setText("");
                new User_Feed(uid).setVisible(true);
                this.dispose();
            }
            catch(SQLException e)
            {
                e.printStackTrace();
                System.out.print(e);
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Title field and Post feed cannot be empty!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

