    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con=DBUtil.getConnection();
            PreparedStatement pr=null,pr1=null;
            if(jButton2.getText().equals("Like"))
            {
               pr=con.prepareStatement("insert into like_counter values("+fid+","+uid+")");
               pr1=con.prepareStatement("update feed set likecount=likecount+1 where feedid="+fid);
               jButton2.setText("Liked");
            }
            else
            {
                pr=con.prepareStatement("delete from like_counter where fid="+fid+" and uid="+uid);
                pr1=con.prepareStatement("update feed set likecount=likecount-1 where feedid="+fid);
                jButton2.setText("Like");
            }
            pr.execute();
            pr1.execute();
        }
        catch(SQLException e)
        {
            e.printStackTrace();
            System.out.print(e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

