    String title,post,un;
    private void loadData() {
        try
        {
            Connection con=DBUtil.getConnection();
            PreparedStatement pr=con.prepareStatement("Select feed.title, feed.post, user.uname "
                    + "from feed join user on feed.user_UID=user.uid where FeedID="+fid);
            ResultSet rs=pr.executeQuery();
            while(rs.next())
            {
                title=rs.getString("title");
                post=rs.getString("post");
                un=rs.getString("uname");
            }
            jTextField1.setText(title);
            jEditorPane1.setText(post);
            jTextField2.setText("Post By: "+un);
            
            PreparedStatement pr1=con.prepareStatement("select * from like_counter where fid="+fid+" and uid="+uid);
            ResultSet rs1=pr1.executeQuery();
            
            if(rs1.next()==true)
            {
                jButton2.setText("Liked");
            }
            else
            {
                jButton2.setText("Like");
            }
                    
        }
        catch(SQLException e)
        {
            e.printStackTrace();
            System.out.print(e);
        }
    }
    
    
