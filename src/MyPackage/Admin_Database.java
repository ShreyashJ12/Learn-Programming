    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String com=comt.getText().trim().toLowerCase();
        
        while(com.endsWith(";"))
             com=com.substring(0, com.length() - 1);
        if(com.startsWith("select") || com.startsWith("insert") || com.startsWith("update") || com.startsWith("delete"))
        {    
            
            try
            {
                Connection con=DBUtil.getConnection();
                ResultSet rs=null;
                PreparedStatement pr=con.prepareStatement(com);
                
                if(com.startsWith("select"))
                {
                    rs=pr.executeQuery();
                    jTable1.setModel(DbUtils.resultSetToTableModel(rs));
                    
                }
                else
                    pr.execute();
                
            }
            catch(SQLException e)
            {
                e.printStackTrace();
                rest.append(e+"");
            }
        }
        else
        {
            rest.append("> Invalid Command Entered! \n");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

