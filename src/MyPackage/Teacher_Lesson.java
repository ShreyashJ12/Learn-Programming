    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String fn=null,ln=null;
        try
        { 
            Connection con=DBUtil.getConnection();
            PreparedStatement pr=con.prepareStatement("select fname, lname from teacher where tid="+tid);
            ResultSet rs=pr.executeQuery();
            while(rs.next())
            {
                fn=rs.getString("fname");
                ln=rs.getString("lname");
            }
           
            new Learn(fn+" "+ln,jtftitle.getText(),"<pre>"+jeplesson.getText()+"</pre>",example,examplefilename,glink,ylink).setVisible(true);
            
        }
        catch(SQLException e){}
    }//GEN-LAST:event_jButton2ActionPerformed

