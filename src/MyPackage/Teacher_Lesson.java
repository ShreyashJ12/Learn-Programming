    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            title=jtftitle.getText();
            String class1 = class2.getText();
            lesson=jeplesson.getText().replaceAll("'", "");
            Connection con=DBUtil.getConnection();
            PreparedStatement pr=con.prepareStatement("insert into lessons values(null,'"+tid+"','"+title+"','"+lesson+"','"+example+"','"+examplefilename+"','"+glink+"','"+ylink+"','"+class1+"')");
            
            pr.execute();
            jtftitle.setText("");
            jeplesson.setText("");
            glink=null;
            ylink=null;
            example=null;
            examplefilename=null;
            JOptionPane.showMessageDialog(this, "Lesson Added Successfully!!");
            
        }   
        catch(SQLException e)
        {
            e.printStackTrace();
            System.out.print(e);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        
        try
        {
            JFileChooser chooser=new JFileChooser();
           int value = chooser.showOpenDialog(null);
           
           if(value == JFileChooser.APPROVE_OPTION)
           {
             XWPFDocument doc = new XWPFDocument(new FileInputStream(chooser.getSelectedFile()));
             XWPFWordExtractor extract=new XWPFWordExtractor(doc);
            
             jeplesson.setText(extract.getText());
           }
            
            
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
            
        }
    }//GEN-LAST:event_jButton5MouseClicked
    
